    private void jBTbulActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBTbulActionPerformed
       isim = jTFad2.getText();
       Node gecici = liste.seciliBul(isim);
       String numaralar="";
       if(gecici==null)
           jTFsonuc.setText("This person is not here!");
       else{
            int size = gecici.getBas().getNumaralar().size();
            int i=0;
            while(i<size)
            {
                 numaralar += gecici.getBas().getNumaralar().get(i)+"\n";
                 i++;
            }
             jTFsonuc.setText(gecici.getBas().getAdSoyad()+" numbers : "+numaralar);
       }
    }//GEN-LAST:event_jBTbulActionPerformed

