    public void tabloyaEkle()      // 1.tabloya verileri girer.
    {
        Node bilgi;
        int i,size;
        String ad,numara;
        
        bilgi = liste.dondur(0);
        while(dtm.getRowCount()>0)
        {
            for(i=0;i<dtm.getRowCount();i++)
            {
                dtm.removeRow(i);
            }
        }
        i=0;
        while(bilgi.getSonraki()!=null)
        {
        
        bilgi = liste.dondur(i);
        ad =bilgi.getBas().getAdSoyad();
        numara=bilgi.getBas().getTumNo() ;   
        dtm.addRow(new String []{ad,numara});
        i++;
        }
    }
    public void tabloyaEkle2()     // 2. tabloya verileri girer.
    {
        Node bilgi;
        int i,size;
        String ad,numara;
        
        bilgi = liste.dondur(0);
        while(dtm2.getRowCount()>0)
        {
            for(i=0;i<dtm2.getRowCount();i++)
            {
                dtm2.removeRow(i);
            }
        }
        
        bilgi = liste.getTail();
        while(bilgi!=null)
        {        
        ad =bilgi.getBas().getAdSoyad();
        numara=bilgi.getBas().getTumNo() ;   
        dtm2.addRow(new String []{ad,numara});
        bilgi=bilgi.getOnceki();       
        }
    }

