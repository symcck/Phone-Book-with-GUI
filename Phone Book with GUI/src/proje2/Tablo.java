        int satirSay = dosya.satirSayisi();
        int i=0;
            
        numaralar.add("bos");
        liste.basaekle("aaaaaaaaaaaaaa", numaralar);      // Listeye alfabetik bir sekilde eleman ekleyebilmek icin
        liste.sonaekle("zzzzzzzzzzzzz", numaralar);      //Sonradan kaldirilmak uzere gecici bir alfabetik head ve tail ataniyor.
